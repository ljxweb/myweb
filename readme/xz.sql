DROP DATABASE IF EXISTS `xz`;
CREATE DATABASE `xz` DEFAULT CHARACTER SET UTF8;
USE `xz`;
-- 创建用户表
CREATE TABLE `user`(
    `id` INT KEY AUTO_INCREMENT,
    `uname` VARCHAR(40),
    `upwd` VARCHAR(40),
    `age` TINYINT(3)
)DEFAULT CHARACTER SET UTF8;

-- 创建商品表
CREATE TABLE `product`(
    `id` INT KEY AUTO_INCREMENT,
    `pname` VARCHAR(40),
    `title` VARCHAR(40),
    `price` VARCHAR(40)
)DEFAULT CHARACTER SET UTF8;

